def u_model(base):
    """推力占比→稳态航速映射 [m/s]（阶段三领航者标定 u_ss≈0.96√base），
    用于外定位断流时的模型辅助航位推算。装机后按 P1 推力标定曲线更新。"""
    s = 1.0 if base >= 0 else -1.0
    return 0.96 * math.sqrt(abs(base)) * s


